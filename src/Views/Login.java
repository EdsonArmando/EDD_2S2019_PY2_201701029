    public LinkedList<NodeAVL> listadoArchivos(NodoContenido listado) {
        if (listado == null) {
            return null;
        }
        listado.avl.tempo.clear();
        listado.avl.reco(listado.raiz);
        LinkedList<NodeAVL> nuevo = listado.avl.devLista();
        return nuevo;
    }

